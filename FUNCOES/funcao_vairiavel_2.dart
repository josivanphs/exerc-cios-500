
// calculladora
main() {
  var adicao = (int a, int b) => a + b;
  var subtracao = (int a, int b) => a - b;
  var multiplicacao = (int a, int b) => a * b;
  var divisao = (int a, int b) => a / b;

  print(adicao(2, 5));
  print(subtracao(2, 5));
  print(multiplicacao(2, 5));
  print(divisao(2, 5));
}
